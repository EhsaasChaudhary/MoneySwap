// Create the iconPaths object dynamically
const iconPaths: Record<string, { icon: string }> = {};
allCurrencies.forEach((currency) => {
  if (icons[currency]) {
    iconPaths[currency] = icons[currency];
  } else {
    console.warn(`Icon for ${currency} not found.`);
  }
});

interface CurrencyIconProps {
  currency: keyof typeof iconPaths;
}

export default function CurrencyIcon({ currency }: CurrencyIconProps) {
  const iconData = iconPaths[currency]?.icon;

  if (!iconData) {
    console.error(`Icon for currency ${currency} not found.`);
    return null;
  }

  return (
    <motion.div
      style={{ width: "30px", height: "30px" }}
      initial={{ scale: 0 }}
      animate={{ scale: 1, rotate: 360 }}
      transition={{ type: "spring", stiffness: 260, damping: 20 }}
    >
      <motion.img
        src={iconData}
        alt={`${currency} icon`}
        style={{ width: "100%", height: "100%" }}
        initial={{ opacity: 0 }}
        animate={{ opacity: 1 }}
        transition={{ duration: 1 }}
      />
    </motion.div>
  );
}
